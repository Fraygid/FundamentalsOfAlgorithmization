temp=float(input('Введите температуру(°C): '))
pressure=int(input('Ввдеите давление(верхние): '))
pulse=int(input('Введите пульс(уд/мин): '))

if 36<=temp<=37 and 110<=pressure<=130 and 60<=pulse<=100:
    print('У вас нормальное состояние.')
elif 35<=temp<=36 and 105<=pressure<=110 and (55<=pulse<=60 or 100<=pulse<=110):
    print('У вас легкое недомогание')
else:
    print('Требуется лечение у врача')
